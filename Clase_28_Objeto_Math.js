console.log(Math);
console.log(Math.PI);
console.log(Math.abs(-7.8)); /* Nos devuelve el valor absoluto */
console.log(Math.ceil(7.2)); /* Redondea al numero mayor */
console.log(Math.floor(7.8)); /* Al numero menor mas cercano */
console.log(Math.round(7.2)); /* Este si redondea como debe ser */
console.log(Math.round(7.8));
console.log(Math.sqrt(8)); /* Raiz */
console.log(
  Math.pow(2, 5)
); /* Potencia, primer valor es la base y el segundo el exponente */
console.log(
  Math.sign(-7.8)
); /* Indicador de si el numero es positivo da un uno positivo pero si es un numero negativo devuelve un uno positivo */
console.log(Math.random()); /* Un numero random entre 0 y 1 */
console.log(Math.random() * 1000); /* Un numero random entre 1 y 1000 */
console.log(Math.round(Math.random() * 1000));
