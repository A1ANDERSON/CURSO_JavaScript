// /^[a-zA-Z]+$/ // valida que una cadena contenga solamente letras (mayúsculas o minúsculas)

// /^[a-zA-Z\s]+$/ // valida que ua cadena contenga letras mayusculas, minusculas y espacios

// /^\d+$/ // valida que una cadena contenga solamente números

// /^[a-zA-Z\d]+$/ // valida que una cadena contenga solamente letras y números (mayúsculas o minúsculas)

// /^\w+$/ // valida que una cadena contenga solamente letras, números y guiones bajos (_)

// /^[a-zA-Z\s]+$/ // valida que una cadena contenga solamente letras y espacios

// /^[\w-\.]+@([\w-]+\.)+[\w-]{2,4}$/ // valida que una cadena contenga una dirección de correo electrónico válida

// /^(\d{4}-\d{2}-\d{2})$/ // valida que una cadena contenga una fecha con formato YYYY-MM-DD

// /^\d{3}-\d{2}-\d{4}$/ // valida que una cadena contenga un número de seguridad social con formato XXX-XX-XXXX

// /^\d{3}\.\d{3}\.\d{3}\-\d{2}$/ // valida que una cadena contenga un número de identificación fiscal (CPF) brasileño con formato XXX.XXX.XXX-XX

// /^\d{2}\.\d{3}\.\d{3}\/\d{4}\-\d{2}$/ // valida que una cadena contenga un número de identificación fiscal (CNPJ) brasileño con formato XX.XXX.XXX/XXXX-XX
